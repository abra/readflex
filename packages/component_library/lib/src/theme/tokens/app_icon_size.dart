/// Icon size tokens aligned with Material Design 3.
///
/// Material uses two standard sizes: 20 (small) and 24 (default). We
/// additionally expose [xs] for dense controls and [lg] for narrow glyphs
/// such as chevrons that need a larger box to read like a default icon.
abstract final class AppIconSize {
  static const double xs = 16;
  static const double sm = 20;
  static const double md = 24;
  static const double lg = 28;
}
